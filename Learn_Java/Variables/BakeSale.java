package Learn_Java.Variables;

public class BakeSale {
    public static void main(String[] args) {
        int numCookies = 17;
        numCookies -= 3; // Customer bought 3 cookies.
        numCookies /= 2; // Customer buys hald the remaining cookies.

        /*
        Compound Assignment Operators:
        - Addition: +=
        - Subtraction: -=
        - Multiplication: *=
        - Division: /=
        - Modulo: %=
        */

        System.out.println(numCookies);
    }
}
