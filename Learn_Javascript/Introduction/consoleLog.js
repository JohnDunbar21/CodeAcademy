// Creates a random number using built-in Math functions and multiplies it by 100.
console.log(Math.random() * 100);

// Creates a random number and multiplies it by 100, then floors it (rounds down) to nearest integer.
console.log(Math.floor(Math.random() * 100));

// Rounds up to the nearest integer, in this case 43.8 becomes 44.
console.log(Math.ceil(43.8));

// This built-in function returns a boolean, checking if the information provided is an integer or other data type.
